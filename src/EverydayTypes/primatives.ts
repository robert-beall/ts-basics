/**
 * Javascript has 3 primitive types: string, number, and boolean.
 * Typescript has corresponding types with the same names.
 * 
 * String, Number, and Boolean correspond to separate, dedicated types in typescript that
 * are rarely used.
 */

{
    // string
    const s: string = "Hello World";

    // number
    const n: number = 42;

    // boolean
    const b: boolean = true;

    console.log(`s: ${typeof s} = ${s}`);
    console.log(`n: ${typeof n} = ${n}`);
    console.log(`b: ${typeof b} = ${b}`);
}
